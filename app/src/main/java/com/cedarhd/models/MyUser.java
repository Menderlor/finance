package com.cedarhd.models;

public class MyUser {
	public int 编号;
	public String 名称;
	public int 部门;
	public String 部门名称;
	public String avaterUrl;
	public MyUser() {
	}

	public MyUser(int 编号, String 名称, int 部门, String 部门名称,String avaterUrl) {
		super();
		this.编号 = 编号;
		this.名称 = 名称;
		this.部门 = 部门;
		this.部门名称 = 部门名称;
		this.avaterUrl=avaterUrl;
	}
}
