package com.cedarhd.models;

import com.j256.ormlite.field.DatabaseField;

import java.io.Serializable;

public class lt论坛版块 implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = -8428770833961563224L;
	@DatabaseField
	public int 编号 ;
	@DatabaseField
	public String 名称 ;
	@DatabaseField
	public int 上级 ;
	@DatabaseField
	public String 代码 ;
	@DatabaseField
	public int 是否不可改 ;
	@Override
	public String toString() {
		return "lt论坛版块 [编号=" + 编号 + ", 名称=" + 名称 + ", 上级=" + 上级 + ", 代码=" + 代码
				+ ", 是否不可改=" + 是否不可改 + "]";
	}
	public int get编号() {
		return 编号;
	}
	public void set编号(int 编号) {
		this.编号 = 编号;
	}
	public String get名称() {
		return 名称;
	}
	public void set名称(String 名称) {
		this.名称 = 名称;
	}
	public int get上级() {
		return 上级;
	}
	public void set上级(int 上级) {
		this.上级 = 上级;
	}
	public String get代码() {
		return 代码;
	}
	public void set代码(String 代码) {
		this.代码 = 代码;
	}
	public int get是否不可改() {
		return 是否不可改;
	}
	public void set是否不可改(int 是否不可改) {
		this.是否不可改 = 是否不可改;
	}
}
