package com.cedarhd.models;

import com.j256.ormlite.field.DatabaseField;

import java.io.Serializable;

/**
 * 橱柜订单
 * 
 * 测量信息
 * 
 * @author bohr
 * 
 */
public class 测量信息 implements Serializable {
	// id date address designer remark chance staff workflow attachments order
	// type

	/**
	 * 
	 */
	private static final long serialVersionUID = -5536315565417160644L;

	@DatabaseField(generatedId = true, unique = true)
	int _Id;

	@DatabaseField
	int Id;

	@DatabaseField
	String Date; // 时间

	@DatabaseField
	String Address; // 地点

	@DatabaseField
	int Designer; // 设计师

	@DatabaseField
	String Remark; // 备注

	@DatabaseField
	int Chance; // 销售机会

	@DatabaseField
	int Staff; // 制单人

	@DatabaseField
	int Workflow; // 工作流

	@DatabaseField
	String Attachments;// 附件

	@DatabaseField
	int Order;// 订单

	@DatabaseField
	int Type;// 测试类型

	@DatabaseField
	public String UpdateTime;

	public int get_Id() {
		return _Id;
	}

	public void set_Id(int _Id) {
		this._Id = _Id;
	}

	public int getId() {
		return Id;
	}

	public void setId(int id) {
		Id = id;
	}

	public String getDate() {
		return Date;
	}

	public void setDate(String date) {
		Date = date;
	}

	public String getAddress() {
		return Address;
	}

	public void setAddress(String address) {
		Address = address;
	}

	public int getDesigner() {
		return Designer;
	}

	public void setDesigner(int designer) {
		Designer = designer;
	}

	public String getRemark() {
		return Remark;
	}

	public void setRemark(String remark) {
		Remark = remark;
	}

	public int getChance() {
		return Chance;
	}

	public void setChance(int chance) {
		Chance = chance;
	}

	public int getStaff() {
		return Staff;
	}

	public void setStaff(int staff) {
		Staff = staff;
	}

	public int getWorkflow() {
		return Workflow;
	}

	public void setWorkflow(int workflow) {
		Workflow = workflow;
	}

	public String getAttachments() {
		return Attachments;
	}

	public void setAttachments(String attachments) {
		Attachments = attachments;
	}

	public int getOrder() {
		return Order;
	}

	public void setOrder(int order) {
		Order = order;
	}

	public int getType() {
		return Type;
	}

	public void setType(int type) {
		Type = type;
	}

	public String getUpdateTime() {
		return UpdateTime;
	}

	public void setUpdateTime(String updateTime) {
		UpdateTime = updateTime;
	}

	@Override
	public String toString() {
		return "测量信息 [_Id=" + _Id + ", Id=" + Id + ", Date=" + Date
				+ ", Address=" + Address + ", Designer=" + Designer
				+ ", Remark=" + Remark + ", Chance=" + Chance + ", Staff="
				+ Staff + ", Workflow=" + Workflow + ", Attachments="
				+ Attachments + ", Order=" + Order + ", Type=" + Type
				+ ", UpdateTime=" + UpdateTime + "]";
	}

}
