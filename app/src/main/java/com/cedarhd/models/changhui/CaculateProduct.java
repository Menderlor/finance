package com.cedarhd.models.changhui;

/**
 * 收益计算器计算要上传的实体类
 * 
 * @author Win
 * 
 */
public class CaculateProduct {

	private int ProductId; // 产品Id
	private int Period;// 期限(月)
	private double Amount; // 投资金额
	private String PayingTime; // 打款时间
	private String Category; // 产品分类

	public int getProductId() {
		return ProductId;
	}

	public void setProductId(int productId) {
		ProductId = productId;
	}

	public int getPeriod() {
		return Period;
	}

	public void setPeriod(int period) {
		Period = period;
	}

	public double getAmount() {
		return Amount;
	}

	public void setAmount(double amount) {
		Amount = amount;
	}

	public String getPayingTime() {
		return PayingTime;
	}

	public void setPayingTime(String payingTime) {
		PayingTime = payingTime;
	}

	public String getCategory() {
		return Category;
	}

	public void setCategory(String category) {
		Category = category;
	}

}
